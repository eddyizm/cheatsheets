import time
import animation

@animation.wait('spinner')
def main():
    print('''
    ****************************************************
    ****************************************************
    ****************************************************
    ****************************************************
    
    20 minute eye break. Please look away for 20 seconds
    
    ****************************************************
    ****************************************************
    ****************************************************
    ****************************************************''')
    time.sleep(20)
    return
    
if __name__ == '__main__':
    main()